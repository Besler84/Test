note = int(input("Bitte eine Zahl zwischen 1 und 6 eingeben: "))

if note == 1:
    print("Sehr gut")
elif note == 2:
    print("Gut")
elif note == 3:
    print("Befriedigend")
elif note == 4:
    print("Ausreichend")
elif note == 5:
    print("Mangelhaft")
elif note == 6:
    print("Ungenügend")
else:
    print("Ungültige Eingabe! Bitte eine Zahl von 1 bis 6 eingeben.")